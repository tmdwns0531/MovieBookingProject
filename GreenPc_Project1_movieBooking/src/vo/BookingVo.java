package vo;

public class BookingVo {

	int idx;
	int member_id;
	String booking_theater;
	String booking_time;
	String booking_seat;
	int booking_ranking;
	String booking_movie;
	int booking_year;
	int booking_month;
	int booking_date;
	int booking_price;

	public BookingVo() {
	}

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}

	public int getMember_id() {
		return member_id;
	}

	public void setMember_id(int member_id) {
		this.member_id = member_id;
	}

	public String getBooking_theater() {
		return booking_theater;
	}

	public void setBooking_theater(String booking_theater) {
		this.booking_theater = booking_theater;
	}

	public String getBooking_time() {
		return booking_time;
	}

	public void setBooking_time(String booking_time) {
		this.booking_time = booking_time;
	}

	public String getBooking_seat() {
		return booking_seat;
	}

	public void setBooking_seat(String booking_seat) {
		this.booking_seat = booking_seat;
	}

	public int getBooking_ranking() {
		return booking_ranking;
	}

	public void setBooking_ranking(int booking_ranking) {
		this.booking_ranking = booking_ranking;
	}

	public String getBooking_movie() {
		return booking_movie;
	}

	public void setBooking_movie(String booking_movie) {
		this.booking_movie = booking_movie;
	}

	public int getBooking_year() {
		return booking_year;
	}

	public void setBooking_year(int booking_year) {
		this.booking_year = booking_year;
	}

	public int getBooking_month() {
		return booking_month;
	}

	public void setBooking_month(int booking_month) {
		this.booking_month = booking_month;
	}

	public int getBooking_date() {
		return booking_date;
	}

	public void setBooking_date(int booking_date) {
		this.booking_date = booking_date;
	}

	public int getBooking_price() {
		return booking_price;
	}

	public void setBooking_price(int booking_price) {
		this.booking_price = booking_price;
	}

}
